package com.example.essai;

public class Dates {

    String debut;
    String fin;

    public String getDebut() {
        return this.debut;
    }

    public String setDebut(String deb) {
        this.debut = deb;
        return this.debut;
    }

    public String getFin() {
        return this.fin;
    }

    public String setFin(String fin) {
        this.fin = fin;
        return this.fin;
    }

}
